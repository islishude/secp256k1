package scalar

import (
	"encoding/binary"
	"testing"
)

func FuzzScalarMontgomeryBackend(f *testing.F) {
	f.Add(make([]byte, 64))
	f.Add([]byte{
		0x41, 0x41, 0x36, 0xd0, 0x8c, 0x5e, 0xd2, 0xbf,
		0x3b, 0xa0, 0x48, 0xaf, 0xe6, 0xdc, 0xae, 0xba,
		0xfe, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
		0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
		0xef, 0xcd, 0xab, 0x89, 0x67, 0x45, 0x23, 0x01,
		0x10, 0x32, 0x54, 0x76, 0x98, 0xba, 0xdc, 0xfe,
		0xaa, 0x55, 0xaa, 0x55, 0xaa, 0x55, 0xaa, 0x55,
		0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0x7f,
	})
	f.Fuzz(func(t *testing.T, data []byte) {
		if len(data) != 64 {
			return
		}
		var x, y [4]uint64
		for i := range 4 {
			x[i] = binary.LittleEndian.Uint64(data[i*8 : (i+1)*8])
			y[i] = binary.LittleEndian.Uint64(data[32+i*8 : 32+(i+1)*8])
		}
		checkScalarMontgomeryBackend(t, x, y)
	})
}
