#define SELECT_W6_ENTRY(digit) \
	CMP $digit, R2; \
	CSETM EQ, R3; \
	VDUP R3, V4.D2; \
	VLD1.P 64(R1), [V5.B16, V6.B16, V7.B16, V8.B16]; \
	VBIT V4.B16, V5.B16, V0.B16; \
	VBIT V4.B16, V6.B16, V1.B16; \
	VBIT V4.B16, V7.B16, V2.B16; \
	VBIT V4.B16, V8.B16, V3.B16

