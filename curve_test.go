package secp256k1

import (
	"testing"

	"github.com/islishude/secp256k1/internal/field"
	"github.com/islishude/secp256k1/internal/scalar"
)

func TestSEC2Parameters(t *testing.T) {
	expectedP := [32]byte{
		0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
		0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
		0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
		0xff, 0xff, 0xff, 0xfe, 0xff, 0xff, 0xfc, 0x2f,
	}
	expectedN := [32]byte{
		0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
		0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xfe,
		0xba, 0xae, 0xdc, 0xe6, 0xaf, 0x48, 0xa0, 0x3b,
		0xbf, 0xd2, 0x5e, 0x8c, 0xd0, 0x36, 0x41, 0x41,
	}
	expectedGX := [32]byte{
		0x79, 0xbe, 0x66, 0x7e, 0xf9, 0xdc, 0xbb, 0xac,
		0x55, 0xa0, 0x62, 0x95, 0xce, 0x87, 0x0b, 0x07,
		0x02, 0x9b, 0xfc, 0xdb, 0x2d, 0xce, 0x28, 0xd9,
		0x59, 0xf2, 0x81, 0x5b, 0x16, 0xf8, 0x17, 0x98,
	}
	expectedGY := [32]byte{
		0x48, 0x3a, 0xda, 0x77, 0x26, 0xa3, 0xc4, 0x65,
		0x5d, 0xa4, 0xfb, 0xfc, 0x0e, 0x11, 0x08, 0xa8,
		0xfd, 0x17, 0xb4, 0x48, 0xa6, 0x85, 0x54, 0x19,
		0x9c, 0x47, 0xd0, 0x8f, 0xfb, 0x10, 0xd4, 0xb8,
	}

	if secp256k1A != 0 || secp256k1B != 7 || secp256k1H != 1 {
		t.Fatalf("unexpected curve constants: a=%d b=%d h=%d", secp256k1A, secp256k1B, secp256k1H)
	}
	if field.Modulus != expectedP {
		t.Fatalf("unexpected field modulus\n got %x\nwant %x", field.Modulus, expectedP)
	}
	if scalar.Order != expectedN {
		t.Fatalf("unexpected group order\n got %x\nwant %x", scalar.Order, expectedN)
	}
	if gxBytes != expectedGX || gyBytes != expectedGY {
		t.Fatalf("unexpected generator\n got (%x,%x)\nwant (%x,%x)", gxBytes, gyBytes, expectedGX, expectedGY)
	}
	if generator.isInfinity() || !isOnCurve(&generator.x, &generator.y) {
		t.Fatal("SEC2 generator is not a valid non-infinity curve point")
	}
}
